# .split => içerisinde verilen parametreye göre soldan sağa doğru ayırma işlemi gerçekleştirir

elemanlar = "yazılım,sistem,teknik,çizim,web,grafik"
print(elemanlar)

elemanlar = elemanlar.split()
print(elemanlar)

elemanlar = "yazılım,sistem,teknik,çizim,web,grafik"
elemanlar  = elemanlar.split(',')
print(elemanlar[:])